countries = []
for country in un_countries:
    pycountry_item = pycountry.countries.search_fuzzy(country["name"])[0]
    countries.append({
        "name": pycountry_item.name,
        "country_small_name": country.get("country_small_name"),
        "code": pycountry_item.alpha_2,
        "flag": pycountry_item.flag
    })

calendar = []
holiday_api_key = '647bed32-c177-4972-9bd6-306c692425d5'
hapi = holidayapi.v1(holiday_api_key)

for country in countries:
    print(country["name"])
    pyc = pycountry.countries.get(alpha_2 = country["code"])
    holidays = hapi.holidays({"country": country["code"].upper(), 'year': '2022'})['holidays']
    for holiday in holidays:
        if holiday['public'] and (holiday['date'] == holiday['observed']):
            calendar.append({"date": holiday['date'], "name": holiday['name'], "country": pyc.name, "country_small_name": country.get("country_small_name"), "flag": pyc.flag})

with open('calendar.csv', 'w', newline='') as csvfile:
    fieldnames = ['date', 'name', 'country', 'country_small_name', 'flag']
    writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
    writer.writeheader()
    for day in calendar:
        writer.writerow({"date": day['date'], "name": day['name'], "country": day['country'], "country_small_name": day["country_small_name"], "flag": day['flag']})
